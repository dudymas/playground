package grey

import "testing"

func TestToGrey(t *testing.T) {
	fixtures := []struct{ in, out uint }{
		{0x0, 0x0},
		{0x1, 0x1},
		{0x2, 0x3},
		{0x3, 0x2},
		{0x4, 0x6},
		{0x5, 0x7},
		{0x6, 0x5},
		{0x7, 0x4},
		{0x8, 0xC},
		{0x9, 0xD},
		{0xA, 0xF},
		{0xB, 0xE},
		{0xC, 0xA},
		{0xD, 0xB},
		{0xE, 0x9},
		{0xF, 0x8},
	}
	for _, tt := range fixtures {
		result := ToGrey(tt.in)
		if result != tt.out {
			t.Errorf("ToGrey(%v) failed! expected: %v got: %v", tt.in, tt.out,
				result)
		}
	}
}

